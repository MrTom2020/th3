package lethanhhiep1824801030217;

public class Point
{
	private int x = 0 ,y = 0;
	public Point()
	{
		
	}
	public Point(int X,int Y)
	{
		this.x = X;
		this.y = Y;
	}
	public int getX()
	{
		return this.x;
	}
	public int getY()
	{
		return this.y;
	}
	public void setX(int X)
	{
		this.x = X;
	}
	public void setY(int Y)
	{
		this.y = Y;
	}
}
